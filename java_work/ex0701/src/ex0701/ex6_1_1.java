package ex0701;

public class ex6_1_1 {

	public static void main(String[] args) {
		
		num(5,3);
		
	}
	
	public static void num(int a, int b) {
		System.out.println("+ = "+ (a+b));
		System.out.println("- = "+ (a-b));
		System.out.println("* = "+ (a*b));
		System.out.println("/ = "+ (a/b)+".."+(a%b));
	}
	
}
